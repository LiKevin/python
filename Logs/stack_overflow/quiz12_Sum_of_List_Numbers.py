# demo 1: reduce:
########################################################################################################################
def _sum_1(lst):
    return reduce(lambda x, y: x+y, lst)

# demo 2: sum:
########################################################################################################################
def _sum_2(lst):
    return sum(lst)

# demo 3: function:
########################################################################################################################
def _sum_3(lst):
    amount = 0

    def add(x, y):
        return x+y

    for i in lst:
        amount = add(i, amount)
    return amount

