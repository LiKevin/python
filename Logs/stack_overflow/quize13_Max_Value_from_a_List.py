# demo 1:  reduce:
########################################################################################################################
def _max_1(lst):

    return reduce(lambda x, y: x>y and x or y, lst)

# demo 2:  max
########################################################################################################################
def _max_2(lst):
    return max(lst)

# demo 3:  function:
########################################################################################################################
def _max_3(lst):

    max = lst[:].pop(0)
    for i in lst:
        max = i>max and i or max
    return max

# demo 4:  sorted:
########################################################################################################################
def _max_4(lst):
    return sorted(lst)[-1]


