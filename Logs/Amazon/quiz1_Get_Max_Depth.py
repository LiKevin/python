# function implementations
# purpose is to:
#   求一个数组中间隔最远的两个享用元素间的步长
#######################################################################################################################

# demo 1:  思路：记录每个元素的起始index 和 终止index， 求abs, 然后找出最大的
#######################################################################################################################
def _test_N(lst):

    new_dict = {}
    lst_dict = list(enumerate(lst))

    for key, val in lst_dict:
        if not val in new_dict:
            new_dict[val] = (key, key)
        else:
            new_dict[val] = (new_dict[val][0], key)

    return  max([abs(v[0]-v[1]) for k, v in new_dict.items()])

